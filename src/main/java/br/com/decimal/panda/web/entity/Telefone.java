package br.com.decimal.panda.web.entity;

public class Telefone {

	private Long ddi;
	private Long ddd;
	private Long numero;
	private Long ramal;

	public Long getDdi() {
		return ddi;
	}

	public void setDdi(Long ddi) {
		this.ddi = ddi;
	}

	public Long getDdd() {
		return ddd;
	}

	public void setDdd(Long ddd) {
		this.ddd = ddd;
	}

	public Long getNumero() {
		return numero;
	}

	public void setNumero(Long numero) {
		this.numero = numero;
	}

	public Long getRamal() {
		return ramal;
	}

	public void setRamal(Long ramal) {
		this.ramal = ramal;
	}

}
